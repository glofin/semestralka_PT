/**
 * Instance tridy {@code Oaza} predstavuji jednotlive oazy, do kterych mohou chodit velbloudi
 */
public class Oaza {

	/** Souradnice x */
	double x;
	/** Souradnice y */
	double y;
	
	/**
	 * Vytvori Oazu na urcenych souradnicich
	 * 
	 * @param x		souradnice x
	 * @param y		souradnice y
	 */
	public Oaza(double x, double y) {
		this.x = x;
		this.y = y;
	}
}
